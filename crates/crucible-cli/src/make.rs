#[cfg(test)]
mod tests {
    use super::*;
    use tempfile::TempDir;

    #[test]
    fn test_pascal_to_snake() {
        assert_eq!(pascal_to_snake("User"), "user");
        assert_eq!(pascal_to_snake("UserProfile"), "user_profile");
        assert_eq!(pascal_to_snake("BlogPost"), "blog_post");
    }

    #[test]
    fn test_next_migration_version_empty_dir() {
        let tmp = TempDir::new().unwrap();
        let dir = tmp.path().join("migrations");
        fs::create_dir(&dir).unwrap();
        let version = next_migration_version_in(&dir).unwrap();
        assert_eq!(version, "001");
    }

    #[test]
    fn test_run_make_migration() {
        let tmp = TempDir::new().unwrap();
        fs::create_dir(tmp.path().join("migrations")).unwrap();

        run_make_migration_in(tmp.path(), "add_posts_table").unwrap();

        let path = tmp.path().join("migrations/001_add_posts_table.sql");
        assert!(path.exists());
        let content = fs::read_to_string(&path).unwrap();
        assert!(content.contains("-- +migrate Up"));
        assert!(content.contains("-- +migrate Down"));
    }

    #[test]
    fn test_run_make_model() {
        let tmp = TempDir::new().unwrap();

        run_make_model_in(tmp.path(), "BlogPost").unwrap();

        let path = tmp.path().join("src/models/blog_post.forge");
        assert!(path.exists());
        let content = fs::read_to_string(&path).unwrap();
        assert!(content.contains("struct BlogPost"));
    }

    #[test]
    fn test_run_make_repo() {
        let tmp = TempDir::new().unwrap();

        run_make_repo_in(tmp.path(), "User").unwrap();

        let path = tmp.path().join("src/repositories/user_repository.forge");
        assert!(path.exists());
        let content = fs::read_to_string(&path).unwrap();
        assert!(content.contains("struct UserRepository"));
    }
}
